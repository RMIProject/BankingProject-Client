    private void bOTPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOTPActionPerformed
        if(!(name.getText().equals("")||recaccno.getText().equals("")||date.getText().equals("")||amount.getText().equals(""))){
            rec.name=name.getText();
            date1=date.getText();
            try{
                rec.acc=Integer.parseInt(recaccno.getText());
                rec.balance=Integer.parseInt(amount.getText());
                
            Client c=new Client();
            try{
                getOTP=Integer.toString(c.stub.getOTP(u.acc));
                if(!getOTP.equals("0"))
                JOptionPane.showMessageDialog(f,"OTP Sent");
                else
                JOptionPane.showMessageDialog(f,"OTP not sent.\nTry Again Later");
            }
            catch(Exception e){JOptionPane.showMessageDialog(f," Unexpected event Occurs.\nTry again Later.","Alert",JOptionPane.WARNING_MESSAGE);}
            }
            catch(NumberFormatException e){ JOptionPane.showMessageDialog(f,"Account and Amount must be Numerical Value.");}
        }
        else
        {
            JOptionPane.showMessageDialog(f,"All Field Required");
        }
    }//GEN-LAST:event_bOTPActionPerformed

