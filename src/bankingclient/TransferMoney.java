    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        if(otp.getText().matches(OTPpattern)){
            if(otp.getText().equals(getOTP)){
                Client c=new Client();
                try{
                    int temp=c.stub.moneyTransfer(u.acc, rec.acc, rec.balance, date1);
                    if(temp!=-1&&temp!=-2){
                        JOptionPane.showMessageDialog(f,"Transfer successful.\n Your balance is "+temp);
                        u.balance=temp;
                        this.setVisible(false);
                        new TransferMoney(backform,u);
                    }
                    else
                    { if(temp==-2)
                             JOptionPane.showMessageDialog(f,"Do not have Enough Money");
                        else
                             JOptionPane.showMessageDialog(f,"Money Transfer Unsuccessful.Try Again Later");
                    }
                }
                catch(Exception e){JOptionPane.showMessageDialog(f," Unexpected event Occurs.\nTry again Later.","Alert",JOptionPane.WARNING_MESSAGE);}
            }
            else
              JOptionPane.showMessageDialog(f,"OTP not match try again Later");    
        }
        else{
            JOptionPane.showMessageDialog(f,"OTP must be of 4 Digit");
        }
    }//GEN-LAST:event_submitActionPerformed

